package domain;

public class Face {
    private int face_id;
    private String face_name;
    private String face_post;
    private String face_img;
    private int face_age;
    private String face_sex;

    public int getFace_id() {
        return face_id;
    }

    public void setFace_id(int face_id) {
        this.face_id = face_id;
    }

    public String getFace_name() {
        return face_name;
    }

    public void setFace_name(String face_name) {
        this.face_name = face_name;
    }

    public String getFace_post() {
        return face_post;
    }

    public void setFace_post(String face_post) {
        this.face_post = face_post;
    }

    public String getFace_img() {
        return face_img;
    }

    public void setFace_img(String face_img) {
        this.face_img = face_img;
    }

    public int getFace_age() {
        return face_age;
    }

    public void setFace_age(int face_age) {
        this.face_age = face_age;
    }

    public String getFace_sex() {
        return face_sex;
    }

    public void setFace_sex(String face_sex) {
        this.face_sex = face_sex;
    }
}
